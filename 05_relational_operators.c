/*
 *  Relational operators
 */

#include <stdio.h>

int main()
{
	if (3 > 1) /* greater */
	{
		printf("3 is greater than 1\n");
	}

	if (3 < 5) /* less */
	{
		printf("3 is less than 5\n");
	}

	if (1 == 1) /* equal */
	{
		printf("1 is equal to 1\n");
	}

	if (2 != 1) /* not equal */
	{
		printf("2 is not equal to 1\n");
	}

	if (3 >= 1) /* greater equal */
	{
		printf("3 is greater than or equal 1\n");
	}

	if (3 <= 5) /* less equal*/
	{
		printf("3 is less than or equal 5\n");
	}
}
